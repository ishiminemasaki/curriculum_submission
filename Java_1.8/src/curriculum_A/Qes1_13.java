package curriculum_A;

public class Qes1_13 {

	public static void main(String[] args) {
		
		byte byteVar = 0;
		// バイト型
		
        short shortVar = 0;
        // 短整数型
        
        int intVar = 0;
        // 整数型
        
        long longVar = 0L;
        // 長整数型（末尾にLを付ける）
        
        float floatVar = 0.0f;
        // 単精度浮動小数点数型（末尾にfを付ける）
        
        double doubleVar = 0.0;
        // 倍精度浮動小数点数型
        
        char charVar = '\u0000';
        // 文字型（charの初期値はヌル文字）
        
        String stringVar = "";
        // 文字列型（空文字で初期化）
        
        boolean booleanVar = false;
        // ブーリアン型（trueまたはfalse）

	}

}
