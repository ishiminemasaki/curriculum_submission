package curriculum_A;

public class Qes1_13 {

	public static void main(String[] args) {
		
		byte byteVar = 10;
		// バイト型 10
		
        short shortVar = 100;
        // 短整数型 100
        
        int intVar = 1000;
        // 整数型 1000
        
        long longVar = 10000L;
        // 長整数型 10000（Lを忘れずに）
        
        float floatVar = 9.5f;
        // 単精度浮動小数点数型 9.5
        
        double doubleVar = 10.5;
        // 倍精度浮動小数点数型 10.5
        
        char charVar = 'a';
        // 文字型 'a'
        
        String stringVar = "ハロー";
        // 文字列型 "ハロー"
        
        boolean booleanVar = true;
        // ブーリアン型 true

	}

}
